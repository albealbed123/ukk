    private void btn_detailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_detailActionPerformed
        // TODO add your handling code here:                                                                                                                              
    int row = tb_riwayat.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data dulu!");
        return;
    }

    try {

        StringBuilder detail = new StringBuilder();
        String pathFoto = null;

        detail.append("DETAIL DATA PENGADUAN\n");
        detail.append("====================================\n");

        for (int i = 0; i < tb_riwayat.getColumnCount(); i++) {

            String namaKolom = tb_riwayat.getColumnName(i);
            Object value = tb_riwayat.getValueAt(row, i);

            if (namaKolom.equalsIgnoreCase("foto")) {
                pathFoto = value == null ? null : value.toString();
            } else {
                detail.append(namaKolom)
                      .append(" : ")
                      .append(value == null ? "-" : value.toString())
                      .append("\n");
            }
        }

        detail.append("====================================");

        // ================= PANEL UTAMA =================
        JPanel panelUtama = new JPanel(new BorderLayout(20, 10));

        // ================= TEXT DETAIL =================
        JTextArea textArea = new JTextArea(detail.toString());
        textArea.setEditable(false);
        textArea.setLineWrap(true);
        textArea.setWrapStyleWord(true);

        JScrollPane scrollText = new JScrollPane(textArea);
        scrollText.setPreferredSize(new Dimension(350, 250));

        panelUtama.add(scrollText, BorderLayout.CENTER);

        // ================= FOTO =================
        if (pathFoto != null && !pathFoto.isEmpty()) {

            java.io.File file = new java.io.File(pathFoto);

            if (file.exists()) {

                ImageIcon icon = new ImageIcon(pathFoto);

                Image img = icon.getImage().getScaledInstance(250, 250, Image.SCALE_SMOOTH);
                JLabel lblFoto = new JLabel(new ImageIcon(img));
                lblFoto.setHorizontalAlignment(JLabel.CENTER);

                panelUtama.add(lblFoto, BorderLayout.EAST);

            } else {
                panelUtama.add(new JLabel("Foto tidak ditemukan"), BorderLayout.EAST);
            }
        }

        JOptionPane.showMessageDialog(this, panelUtama,
                "Detail Pengaduan",
                JOptionPane.PLAIN_MESSAGE);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this,
                "Gagal tampil detail: " + e.getMessage());
    }

    }//GEN-LAST:event_btn_detailActionPerformed

