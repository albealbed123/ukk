    public void load_riwayat() {
    DefaultTableModel model = new DefaultTableModel();

    try {
        String sql = "SELECT * FROM pengaduan "
                   + "WHERE status IS NOT NULL AND status != '' "
                   + "ORDER BY tgl_pengaduan ASC";

        Connection conn = Koneksi.Koneksi.KoneksiDB();
        Statement stm = conn.createStatement();
        ResultSet res = stm.executeQuery(sql);

        // ðŸ”¥ Ambil metadata kolom otomatis
        java.sql.ResultSetMetaData meta = res.getMetaData();
        int jumlahKolom = meta.getColumnCount();

        // ðŸ”¥ Tambahkan nama kolom otomatis
        for (int i = 1; i <= jumlahKolom; i++) {
            model.addColumn(meta.getColumnName(i));
        }

        // ðŸ”¥ Isi data otomatis
        while (res.next()) {
            Object[] row = new Object[jumlahKolom];
            for (int i = 0; i < jumlahKolom; i++) {
                row[i] = res.getObject(i + 1);
            }
            model.addRow(row);
        }

        tb_riwayat.setModel(model);
        lbl_total.setText("TOTAL DATA: " + tb_riwayat.getRowCount() + " pengaduan");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal Load Data: " + e.getMessage());
    }

}

