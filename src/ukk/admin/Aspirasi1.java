    private void tbAspirasiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbAspirasiMouseClicked
        // TODO add your handling code here:                                                                                                                                                                                                                                                                                  
    int i = tbAspirasi.getSelectedRow();
    if (i != -1) {
        txt_id.setText(tbAspirasi.getValueAt(i, 0).toString());
        txt_nik.setText(tbAspirasi.getValueAt(i, 1).toString());
        txt_nama.setText(tbAspirasi.getValueAt(i, 2).toString());
        txt_isi.setText(tbAspirasi.getValueAt(i, 3).toString());
        
        // Mengisi data kategori ke ComboBox
        cb_kategori.setSelectedItem(tbAspirasi.getValueAt(i, 4).toString());
        
        // KUNCI DISINI: Agar admin tidak bisa mengubah
        cb_kategori.setEnabled(false); 
        
        cb_status.setSelectedItem(tbAspirasi.getValueAt(i, 5).toString());
        
        try {
            java.util.Date date = new java.text.SimpleDateFormat("yyyy-MM-dd").parse(tbAspirasi.getValueAt(i, 6).toString());
            txt_tanggal.setDate(date);
        } catch (Exception e) {
            txt_tanggal.setDate(null);
        }
    }


    }//GEN-LAST:event_tbAspirasiMouseClicked

