   void tampilId() {
    try {
        String sql = "SELECT IFNULL(MAX(id_petugas), 0) + 1 AS id FROM petugas";
        pst = conn.prepareStatement(sql);
        rs = pst.executeQuery();

        if (rs.next()) {
            txt_id.setText(rs.getString("id"));
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}

